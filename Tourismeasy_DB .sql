CREATE TABLE IF NOT EXISTS DEPARTAMENTOS
(
    ID_DPTO      VARCHAR(2)   NOT NULL,
    NOM_DPTO     VARCHAR(18)  NOT NULL CHECK(LENGTH(NOM_DPTO) >= 3),
    PRIMARY KEY(ID_DPTO)
);

--******************************
-- MAESTRA
--******************************

CREATE TABLE IF NOT EXISTS CIUDADES
(
    ID_CIUDAD      VARCHAR(6)   NOT NULL CHECK(LENGTH(ID_CIUDAD) >= 5),
    NOM_CIUDAD     VARCHAR(27)  NOT NULL CHECK(LENGTH(NOM_CIUDAD) >= 3),
    ID_DPTO        VARCHAR(2)   NOT NULL,
    VAL_ZIP        DECIMAL(6)   NOT NULL CHECK(VAL_ZIP > 0),
    VAL_LATITUD    DECIMAL      NOT NULL,
    VAL_LONGITUD   DECIMAL      NOT NULL,
    PRIMARY KEY(ID_CIUDAD),
    FOREIGN KEY(ID_DPTO) REFERENCES DEPARTAMENTOS(ID_DPTO)
);

--******************************
-- INFORMATIVA / MAESTRA
--******************************

CREATE TABLE IF NOT EXISTS EMPRESAS
(
    ID_EMPRESA              VARCHAR(10)   NOT NULL CHECK(LENGTH(ID_EMPRESA) = 10),
    NOM_EMPRESA             VARCHAR(50)   NOT NULL CHECK(LENGTH(NOM_EMPRESA) >= 2),
    EMAIL_INSTITU_EMPRESA   VARCHAR       NOT NULL CHECK(EMAIL_INSTITU_EMPRESA ~* '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$'),
    NUM_CONTACTO_EMPRESA    DECIMAL(10,0) NOT NULL CHECK(NUM_CONTACTO_EMPRESA >= 1000000),
    ID_CIUDAD               VARCHAR(6)    NOT NULL,
    TIPO_SERVICIO           VARCHAR(20)   NOT NULL DEFAULT 'PLANES' CHECK(TIPO_SERVICIO IN ('VUELOS','HOSPEDAJE','PLANES','AUXILIAR','MULTIPLE')),
    ESTADO_EMPRESA          VARCHAR(10)   NOT NULL DEFAULT 'ACTIVA' CHECK(ESTADO_EMPRESA IN ('ACTIVA','INACTIVA','PENDIENTE')),
    PRIMARY KEY(ID_EMPRESA),
    FOREIGN KEY(ID_CIUDAD) REFERENCES CIUDADES(ID_CIUDAD)
);

