CREATE TABLE IF NOT EXISTS PERSONAS
(
    ID_PERSONA          VARCHAR(10)   NOT NULL CHECK(LENGTH(ID_PERSONA) >= 5),
    ID_TIPO_DOC         DECIMAL(1,0)  NOT NULL,
    GENERO_PERSONA      VARCHAR(10)   NOT NULL CHECK(GENERO_PERSONA IN ('MASCULINO','FEMENINO','NO BINARIO','TRANS')),
    NOM_PERSONA         VARCHAR       NOT NULL CHECK(LENGTH(NOM_PERSONA) >= 2),
    APE_PERSONA         VARCHAR       NOT NULL CHECK(LENGTH(APE_PERSONA) >= 2),
    NUM_CEL_PERSONA     DECIMAL(10,0) NOT NULL CHECK(NUM_CEL_PERSONA >= 3000000000),
    FECHA_NACIMIENTO    DATE          NOT NULL CHECK(FECHA_NACIMIENTO < CURRENT_DATE),
