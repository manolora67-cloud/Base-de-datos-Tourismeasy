--******************************
-- OPERACIONAL: USUARIOS (Viajeros/Admin)
--******************************

CREATE TABLE IF NOT EXISTS USUARIOS
(
    ID_USUARIO    DECIMAL(5,0) NOT NULL,
    ID_PERSONA    VARCHAR(10)  NOT NULL,
    ROL           VARCHAR(10)  NOT NULL DEFAULT 'viajero' CHECK(ROL IN ('viajero','admin')),
    EMAIL_LOGIN   VARCHAR      NOT NULL CHECK(EMAIL_LOGIN ~* '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$'),
    -- Min 8 caracteres, solo letras, números, + y -
    CONTRASENA    VARCHAR(64)  NOT NULL UNIQUE 
                  CHECK(LENGTH(CONTRASENA) >= 8 AND CONTRASENA ~ '^[A-Za-z0-9+-]+$'),
    ESTADO        VARCHAR(10)  NOT NULL DEFAULT 'ACTIVO' CHECK(ESTADO IN ('ACTIVO','INACTIVO','PENDIENTE')),
    PRIMARY KEY(ID_USUARIO),
    FOREIGN KEY(ID_PERSONA) REFERENCES PERSONAS(ID_PERSONA)
);

