CREATE TABLE IF NOT EXISTS FACTURA_ENCABEZADO_VENTA
(
    ID_FAC_ENCA_VEN   DECIMAL(10,0) NOT NULL,
    FECHA_EMISION     DATE          NOT NULL DEFAULT CURRENT_DATE,
    ID_USUARIO        DECIMAL(5,0)  NOT NULL,
    ID_RESERVA        DECIMAL(10,0) NOT NULL,
    ID_CIUDAD         VARCHAR(6)    NOT NULL,
    ESTADO_PAGO       VARCHAR(9)    NOT NULL CHECK(ESTADO_PAGO IN ('PAGO','PENDIENTE','CANCELADO')), -- PAGO / PENDIENTE / CANCELADO
    PRIMARY KEY(ID_FAC_ENCA_VEN),
    FOREIGN KEY(ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO),
    FOREIGN KEY(ID_RESERVA) REFERENCES RESERVAS(ID_RESERVA),
    FOREIGN KEY(ID_CIUDAD) REFERENCES CIUDADES(ID_CIUDAD)
);

--******************************
-- TRANSACCIONAL
--******************************

CREATE TABLE IF NOT EXISTS FACTURA_DETALLE_VENTA
(
    ID_FAC_DET_VEN     DECIMAL(10,0) NOT NULL,
    ID_FAC_ENCA_VEN    DECIMAL(10,0) NOT NULL,
    LINEA_ITEM         DECIMAL(2,0)  NOT NULL CHECK(LINEA_ITEM > 0),
    ID_PLAN_TURISTICO  DECIMAL(5,0)  NOT NULL,
    PRECIO_UNITARIO    DECIMAL(12,0) NOT NULL CHECK(PRECIO_UNITARIO >= 0),
    ID_FORMA_PAGO      DECIMAL(1,0)  NOT NULL,
    SUB_TOTAL          DECIMAL(12,0) NOT NULL CHECK(SUB_TOTAL >= 0),
    DESCUENTO          DECIMAL(12,0) NOT NULL DEFAULT 0 CHECK(DESCUENTO >= 0),
    VALOR_IVA          DECIMAL(5,2)  NOT NULL CHECK(VALOR_IVA >= 0), -- Porcentaje (Ej: 19.00)
    VALOR_TOTAL        DECIMAL(12,0) NOT NULL CHECK(VALOR_TOTAL >= 0),
    PRIMARY KEY(ID_FAC_DET_VEN),
    FOREIGN KEY(ID_FAC_ENCA_VEN) REFERENCES FACTURA_ENCABEZADO_VENTA(ID_FAC_ENCA_VEN),
    FOREIGN KEY(ID_PLAN_TURISTICO) REFERENCES PLANES_TURISTICOS(ID_PLAN_TURISTICO),
    FOREIGN KEY(ID_FORMA_PAGO) REFERENCES FORMAS_DE_PAGO(ID_FORMA_PAGO)
);

--******************************
-- AUDITORIA
--******************************

CREATE TABLE IF NOT EXISTS AUDITORIA
(
    ID_AUDITORIA       SERIAL          NOT NULL,
    FECHA_HORA         TIMESTAMP       NOT NULL DEFAULT NOW(),
    ID_USUARIO         DECIMAL(5,0),
    ROL_USUARIO        VARCHAR(10),
    ACCION             VARCHAR(20)     NOT NULL, -- LOGIN / LOGOUT / INSERT / UPDATE / DELETE / INACTIVAR / RESERVA / PAGO
    TABLA_AFECTADA     VARCHAR(30),
    ID_REGISTRO        VARCHAR(20),
    DESCRIPCION        VARCHAR(200),
    IP_ORIGEN          VARCHAR(45),
    RESULTADO          VARCHAR(10)     NOT NULL DEFAULT 'EXITOSO' CHECK(RESULTADO IN ('EXITOSO','FALLIDO')), -- EXITOSO / FALLIDO
    PRIMARY KEY(ID_AUDITORIA),
    FOREIGN KEY(ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO)
);

