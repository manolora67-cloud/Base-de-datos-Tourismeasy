CREATE TABLE IF NOT EXISTS RESERVAS
(
    ID_RESERVA          DECIMAL(10,0) NOT NULL,
    ID_USUARIO          DECIMAL(5,0)  NOT NULL,
    FECHA_RESERVA       DATE          NOT NULL DEFAULT CURRENT_DATE,
    MENORES             BOOLEAN       NOT NULL, -- TRUE= SI VIAJAN / FALSE= NO VIAJAN
    NUM_MENORES         DECIMAL(2,0)  NOT NULL CHECK(NUM_MENORES >= 0),
    ID_PLAN_TURISTICO   DECIMAL(5,0)  NOT NULL,
    NUM_USUARIOS        DECIMAL(2,0)  NOT NULL CHECK(NUM_USUARIOS > 0),
    ID_FORMA_PAGO       DECIMAL(1,0)  NOT NULL,
    PRIMARY KEY(ID_RESERVA),
    FOREIGN KEY(ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO),
    FOREIGN KEY(ID_PLAN_TURISTICO) REFERENCES PLANES_TURISTICOS(ID_PLAN_TURISTICO),
    FOREIGN KEY(ID_FORMA_PAGO) REFERENCES FORMAS_DE_PAGO(ID_FORMA_PAGO)
);

--******************************
-- SOPORTE
-- Soporte que la empresa atiende al viajero
--******************************

CREATE TABLE IF NOT EXISTS SOPORTE_VIAJERO
(
    ID_SOPORTE          SERIAL          NOT NULL,
    ID_USUARIO          DECIMAL(5,0)    NOT NULL,                   -- viajero que genera la solicitud
    ID_EMPRESA          VARCHAR(10)     NOT NULL,                   -- empresa que debe atenderla
    ID_RESERVA          DECIMAL(10,0),                               -- reserva relacionada (opcional)
    FECHA_SOLICITUD     TIMESTAMP       NOT NULL DEFAULT NOW(),
    MOTIVO              VARCHAR(100)    NOT NULL CHECK(LENGTH(MOTIVO) >= 10), -- descripción breve del problema
    PRIORIDAD           VARCHAR(10)     NOT NULL DEFAULT 'MEDIA' CHECK(PRIORIDAD IN ('ALTA','MEDIA','BAJA')),   -- ALTA / MEDIA / BAJA
    ESTADO              VARCHAR(12)     NOT NULL DEFAULT 'ABIERTA' CHECK(ESTADO IN ('ABIERTA','EN_PROCESO','RESUELTA','CERRADA')), -- ABIERTA / EN_PROCESO / RESUELTA / CERRADA
    RESPUESTA_EMPRESA   VARCHAR(300),                               -- respuesta o acción tomada por la empresa
    FECHA_CIERRE        TIMESTAMP,
    PRIMARY KEY(ID_SOPORTE),
    FOREIGN KEY(ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO),
    FOREIGN KEY(ID_EMPRESA) REFERENCES EMPRESAS(ID_EMPRESA),
    FOREIGN KEY(ID_RESERVA) REFERENCES RESERVAS(ID_RESERVA)
);

